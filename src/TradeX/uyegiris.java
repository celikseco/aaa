        String name;
        String password;
       
        name=jTextField1.getText().trim();
        password=new String(jPasswordField1.getPassword()).trim();
       
        
       
        dbo.dealSave uyeleriCek=new dbo.dealSave();
        ResultSet uye=uyeleriCek.user(name, password);
        try {
            while(uye.next()){
                if (uye.getString("usermail").equals(name)&&uye.getString("userpassword").equals(password)){
                    submit progBaslat=new submit(name,uye.getString("username"),uye.getString("userlastname"));
                    progBaslat.setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(null, "Member does not exist");
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(uyegiris.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
